from typing import Any, List, Optional

from typing_extensions import Self

import zitadel_client.rest_response


class OpenApiError(Exception):
    """The base exception class for all OpenApiErrors"""


class ApiTypeError(OpenApiError, TypeError):
    def __init__(
        self,
        msg: str,
        path_to_item: Optional[List[str]] = None,
        valid_classes: Optional[Any] = None,
        key_type: Optional[bool] = None,
    ) -> None:
        """Raises an exception for TypeErrors

        Args:
            msg (str): the exception message

        Keyword Args:
            path_to_item (list): a list of keys an indices to get to the
                                 current_item
                                 None if unset
            valid_classes (tuple): the primitive classes that current item
                                   should be an instance of
                                   None if unset
            key_type (bool): False if our value is a value in a dict
                             True if it is a key in a dict
                             False if our item is an item in a list
                             None if unset
        """
        self.path_to_item = path_to_item
        self.valid_classes = valid_classes
        self.key_type = key_type
        full_msg = msg
        if path_to_item:
            full_msg = "{0} at {1}".format(msg, render_path(path_to_item))
        super(ApiTypeError, self).__init__(full_msg)


class ApiValueError(OpenApiError, ValueError):
    def __init__(self, msg: str, path_to_item: Optional[List[str]] = None) -> None:
        """
        Args:
            msg (str): the exception message

        Keyword Args:
            path_to_item (list) the path to the exception in the
                received_data dict. None if unset
        """

        self.path_to_item = path_to_item
        full_msg = msg
        if path_to_item:
            full_msg = "{0} at {1}".format(msg, render_path(path_to_item))
        super(ApiValueError, self).__init__(full_msg)


